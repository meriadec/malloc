#include "libft_malloc.h"

void		show_alloc_mem(void)
{
	t_block		*block;

	block = (t_block *)(get_base());

	while (block)
	{
		printf("ZONE %lu\n", block->size);
		printf("%p\n", (void *)block);
		block = block->next;
	}
}
