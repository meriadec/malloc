#include "libft_malloc.h"

t_block		*find_free_block(t_block **last, size_t size)
{
	t_block		*cur;

	cur = (t_block *)(get_base());
	while (cur && !(cur->free && cur->size >= size))
	{
		*last = cur;
		cur = cur->next;
	}
	return cur;
}
